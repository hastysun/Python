# Modules
import random

# Random Choice
print(random.choice(['Ian', 'Pierce', 'Gavin', 'Jon', 'Flodel']))

# Random Integer
# Includes 20
print(random.randint(0, 20))

# Random Range, outputs value from first number to last
# Won't include 10
print(random.randrange(0, 10))
